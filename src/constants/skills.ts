export function normalizeSkill(skill: string): string {
  const normalized = skill.trim().toLowerCase();
  
  // Check aliases
  for (const [canonical, aliases] of Object.entries(SKILL_ALIASES)) {
    if (aliases.some(alias => alias.toLowerCase() === normalized)) {
      return canonical;
    }
  }
  
  // Check exact matches
  const allSkills = [
    ...TECHNICAL_SKILLS.PROGRAMMING_LANGUAGES,
    ...TECHNICAL_SKILLS.FRAMEWORKS_LIBRARIES,
    ...TECHNICAL_SKILLS.DATABASES,
    ...TECHNICAL_SKILLS.CLOUD_PLATFORMS,
    ...TECHNICAL_SKILLS.DEVOPS_TOOLS,
    ...TECHNICAL_SKILLS.VERSION_CONTROL,
    ...TECHNICAL_SKILLS.TESTING_TOOLS,
    ...TECHNICAL_SKILLS.MONITORING_ANALYTICS,
    ...TECHNICAL_SKILLS.PROJECT_MANAGEMENT,
    ...TECHNICAL_SKILLS.DESIGN_TOOLS,
    ...SOFT_SKILLS,
    ...BUSINESS_SKILLS
  ];
  
  const exactMatch = allSkills.find(s => s.toLowerCase() === normalized);
  if (exactMatch) {
    return exactMatch;
  }
  
  // Fuzzy matching for common variations
  if (normalized.includes('react')) return 'React';
  if (normalized.includes('vue')) return 'Vue';
  if (normalized.includes('angular')) return 'Angular';
  if (normalized.includes('node')) return 'Node.js';
  if (normalized.includes('python')) return 'Python';
  if (normalized.includes('java') && !normalized.includes('javascript')) return 'Java';
  if (normalized.includes('javascript') || normalized.includes('js')) return 'JavaScript';
  if (normalized.includes('typescript') || normalized.includes('ts')) return 'TypeScript';
  if (normalized.includes('docker')) return 'Docker';
  if (normalized.includes('kubernetes') || normalized.includes('k8s')) return 'Kubernetes';
  if (normalized.includes('aws') || normalized.includes('amazon')) return 'AWS';
  if (normalized.includes('azure')) return 'Azure';
  if (normalized.includes('google') || normalized.includes('gcp')) return 'GCP';
  if (normalized.includes('git')) return 'Git';
  if (normalized.includes('sql')) return 'SQL';
  
  return skill;
}

